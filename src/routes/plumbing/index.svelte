<h3>Jet Rodding</h3>
<p>While jet rodding itself isn’t a new technology, recent advancements in the field have enabled state of the art equipment to use high-pressure water jets. Which safely clean grease, sludge, and removes tree roots from your sanitary sewer line. Jet rodding brings the pipe back to almost factory condition, without the added expense of digging to repair or replace the pipe. Jet rodding can be utilized on pipes up to 500’ in length and of all sizes from 1.5” pipes to 14” mains, even from a smaller access. This means, jet rodding enables you to clean bigger pipes from smaller access points, which is not possible with a standard power rod.</p>
<h3>Sewer Televising</h3>
<p>Televising is a state of the art technology that allows you to see live footage of the condition of your sewer lines, in order to properly diagnose the problem. You will be able to see if your sewer is broken, separated, or decaying, along with blockages such as tree roots. This equipment also comes with a transmitter that pinpoints the exact location of the camera.</p>
<h3>Power Rodding</h3>
<p>Power rodding is another form of drain cleaning, which uses a steel cable with a cutter blade on the end to break through tree roots or other blockages in the pipe. There are many size machines as well as cutter blades to be used for different drains and circumstances.</p>
